/**
 * Apply a font style to text
 * @param {string} text The text to style
 * @param {number} styleIndex The index of the style (0-29)
 * @returns {string} The styled text
 */
function applyFont(text, styleIndex) {
    if (styleIndex === 0 || !text) return text;

    // Cap index at 29
    const idx = Math.min(Math.max(0, styleIndex), 29);
    const urlRegex = /(https?:\/\/[^\s]+)/gi;

    return text.split(urlRegex).map(part => {
        if (!part) return "";
        if (part.toLowerCase().startsWith('http')) return part; // Don't style URLs
        return part.split('').map(char => {
            if (fontMap[char]) {
                return fontMap[char][idx] || char;
            }
            return char;
        }).join('');
    }).join('');
}

